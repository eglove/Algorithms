const reverseString = function(s) {
  return s.reverse();
};

// Input: ["h","e","l","l","o"]
// Output: ["o","l","l","e","h"]
console.log(reverseString(["h","e","l","l","o"]))

// Input: ["H","a","n","n","a","h"]
// Output: ["h","a","n","n","a","H"]
console.log(reverseString(["H","a","n","n","a","h"]))
